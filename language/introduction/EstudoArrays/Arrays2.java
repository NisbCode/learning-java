package Estudando.introducao.EstudoArrays;

public class Arrays2 {
    public static void main(String[] args) {
        String[] nomes = new String[4];
        nomes[0] = "Carla";
        nomes[1] = "Ana";
        nomes[2] = "Adolfo";
        nomes[3] = "Augusto";
        for (int i = 0; i < nomes.length; i++) {
            System.out.println(nomes[i]);
        }
    }
}

